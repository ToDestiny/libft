#include "libft.h"

int		ft_is_prime(int n)
{
	int		i;

	i = 2;
	if (n < 0 || n == 0 || n == 1)
		return (0);
	if (n > 1)
	{
		while (i < (n / 2) && i <= 46340)
		{
			if (n % i == 0)
				return (0);
			i++;
		}
	}
	return (i);
}
