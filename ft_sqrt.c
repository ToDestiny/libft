#include "libft.h"

int		ft_sqrt(int n)
{
	int		i;
	int		res;

	i = 0;
	res = 0;
	if (n < 0)
		return (0);
	while ((res <= n) && i < n)
	{
		if ((res = i * i) == n)
			return (i);
		i++;
	}
	return (0);
}
